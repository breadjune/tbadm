function koreaFormat(text) {
    let format = '';
    if (text.includes('South Korea')) format += '[한국]';
    if (text.includes('Keywords')) format += '[키워드]';

    if (text.includes('Google')) format += GOOGLE;
    else if (text.includes('Naver')) format += NAVER;
    else if (text.includes('Direct')) format += '다이렉트 (No Referer)';
    else if (text.includes('Daum.net Search')) format += DAUM;
    else if (text.includes('global.11st.co.kr')) format += '[글로벌]' + ST11;
    else if (text.includes('11st.co.kr')) format += ST11;
    else if (text.includes('Coupang.com')) format += COUPANG;
    else if (text.includes('Shopping.naver.com')) format += N_SHOPPING;
    else if (text.includes('WeMakePrice')) format += WMP;
    else if (text.includes('TMON')) format += TMON;
    else if (text.includes('DuckDuckGo')) format += DUCKDUCKGO;
    else if (text.includes('danawa.com')) format += DANAWA;
    else if (text.includes('Mobile Devices')) format += ANDROID;
    else if (text.includes('iPhone')) format += IPHONE;
    else if (text.includes('post.naver.com')) format += N_POST;
    else if (text.includes('sports.news.naver.com')) format += N_SPORTS;
    else if (text.includes('finance.naver.com')) format += N_FINANCE;
    else if (text.includes('smartstore.naver.com')) format += N_SMARTSTORE;
    else if (text.includes('Facebook')) format += FACEBOOK;
    else if (text.includes('YouTube')) format += YOUTUBE;
    else if (text.includes('Instagram')) format += INSTAGRAM;
    else if (text.includes('Twitter')) format += TWITTER;
    else if (text.includes('Wikipedia')) format += WIKIPEDIA;
    else if (text.includes('Twitch.tv')) format += TWITCH;
    else if (text.includes('Amazon.com')) format += AMAZON;
    else if (text.includes('Baidu')) format += BAIDU;
    else if (text.includes('namu.wiki')) format += NAMUWIKI;
    else if (text.includes('gmarket.co.kr')) format += GMARKET;
    else if (text.includes('Tistory.com')) format += TSTORY;
    else if (text.includes('Blogspot')) format += BLOGSPOT;
    else if (text.includes('donga.com')) format += DONGA;
    else if (text.includes('nate.com')) format += NATE;
    else if (text.includes('Reddit')) format += REDDIT;
    else if (text.includes('Ebay.com')) format += EBAY;
    else if (text.includes('Quora')) format += QUORA;
    else if (text.includes('LinkedIn')) format += LINKEDIN;
    else if (text.includes('Tumblr')) format += TUMBLR;
    else if (text.includes('Pinterest')) format += PINTEREST;
    else if (text.includes('Yahoo.com')) format += YAHOO;
    else if (text.includes('Bing.com')) format += BING;
    else if (text.includes('Auction.co.kr')) format += AUCTION;
    else if (text.includes('Inven.co.kr')) format += INVEN;
    else if (text.includes('Chosun.com')) format += CHOSUN;
    else if (text.includes('jobkorea.co.kr')) format += JOPKOREA;
    else if (text.includes('afreecatv.com')) format += AFREECATV;
    else if (text.includes('fmkorea.com')) format += FMKOREA;
    else format += text;
    return format;
}